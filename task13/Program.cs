/* Задача 13: Напишите программу, которая выводит третью цифру заданного числа или сообщает, 
что третьей цифры нет. (Строки и циклы в решении использовать нельзя!)
645 -> 6
78 -> третьей цифры нет
32679 -> 6
*/

// Запрашиваем у пользователя трёхзначное число
Console.Write("Введите трёхзначное число от 100 до 999: ");
int number = Convert.ToInt32(Console.ReadLine());

// Проверяем, что число состоит из трёх цифр
if (number >= 100 && number <= 999)
{
    // Извлекаем третью цифру из числа, используя операторы деления и остатка от деления
    int thirdDigit = number / 100 % 10;

    // Выводим третью цифру на экран, используя строку формата
    Console.Write($"Третья цифра числа {number}: {thirdDigit}");
}
else
{
    // Если число не является трёхзначным, сообщаем об отсутствии третьей цифры
    Console.WriteLine("Третьей цифры нет!");
}
